class Pessoa{

	//Atributos da classe
	private int idade;
	private string nome;

	//Construtor da classe
	public Pessoa(int i, string n){
		idade = i;
		nome = n;
	}

	//Métodos de acesso
	public int getIdade(){
		return idade;
	}
	public void setIdade(int i){
		idade = i;
	}
	public string getNome(){
		return nome;
	}
	public void setNome(string n){
		nome = n;
	}

	//Demais Métodos
	

}